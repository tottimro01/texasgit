						<li>
							เต่าคู่: การเดิมพักข้างโต๊ะที่ไม่บังคับ. ไพ่ 2 ใบแรกด้านไหนด้านหนึ่งที่ได้เป็นคู่แต่ไม่ใช่ดอกและสีเดียวกันถือว่าเป็นเต่าคู่. คุณจะได้รับโบนัส.
