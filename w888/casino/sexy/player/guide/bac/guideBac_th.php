						</li>
						<li>
							Player/Bankerคู่: ถ้าไพ่ 2 ใบแรกของ "Player" หรือ "Banker" เหมือนกัน (ขึ้นอยู่กับ อักษร หรือ เลข,ไม่รวมไพ่ชุด) ถือว่าเป็นไพ่คู่. ถ้าไพ่2ใบแรกของ "Banker" เหมือนกันก็ถือว่าเป็น "Banker คู่". ถ้าไพ่ 2 ใบแรกของ "Player" เหมือนกันก็ถือว่าเป็น"Player คู่".
