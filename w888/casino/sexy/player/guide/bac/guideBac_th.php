					<h3>การสับไพ่</h3>
					<p>
						ในทุกเกมจะใช้ไพ่ใหม่ที่สับแล้ว 8 ชุด. เมื่อไพ่ใหม่มาถึงที่โต๊ะไพ่จะถูกวางในกล่องและเจ้ามือจะเสียบไพ่แยกไหวในไพ่ใหม่. เมื่อไพ่แยกถูกแจกออกมาก็หมายความว่าไพ่ชุดใหม่จะถูกเปลี่ยนในรอบต่อไป. ไพ่เก่าที่เหลือทั้งหมดหลังไพ่แยกจะถูกเอออกทุกทีที่ไพ่ใหม่มาถึงที่โต๊ะ. และก่อนที่จะใส่ไพ่ใหม่เข้าไปที่กล่องๆไพ่เจ้ามือจะสับไพ่บนโต๊ะ.
					</p>

