       <p> - ถ้า แต้ม 2 ใบแรก รวมกันได้ต่ำกว่า 5 (0,1,2,3,4,5) ต้องจั่วไพ่เพิ่มอีก 1 ใบ</p>
       <p> - ถ้า แต้ม 2 ใบแรก รวมกันได้ 6 หรือ 7 ไม่ต้องจั่วแล้ว (พอแล้ว)</p>
       <p> - ถ้าแต้ม 2 ใบแรก รวมกันได้ 8 หรือ 9 ไพ่ป๊อก</p>

